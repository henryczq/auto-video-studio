"""Shared AI cover prompt builder."""


def build_ai_cover_prompt(title: str, description: str = "") -> str:
    """Build the image-generation prompt used by all AI cover workflows."""
    title = (title or "").strip()
    if not title:
        raise ValueError("视频标题不能为空")

    desc = (description or "").strip()
    if desc:
        if "视频里演示了" in desc or "演示了" in desc or desc.startswith("演示"):
            desc_part = desc
        else:
            desc_part = f"视频里演示了{desc}。"
    else:
        desc_part = ""

    return (
        f"请为短视频生成一张竖屏封面图片（3:4比例），主题是：{title}。{desc_part}\n\n"
        f"封面目标：适合抖音/快手/视频号等手机信息流，第一眼能看懂主题，有点击欲望，但不要像固定模板。\n\n"
        f"文字要求：\n"
        f"1. 必须包含标题文字，文字内容不要改写：'{title}'\n"
        f"2. 标题必须清晰、醒目、可读，手机缩略图也能看清\n"
        f"3. 标题可以放在画面正中、中上、偏上、偏下、下方、左侧或右侧留白区域，由你根据画面构图选择\n"
        f"4. 不要把标题固定贴在最上方，也不要每次做成横跨顶部的页眉/横幅；标题应和主体画面形成整体构图，四周保留舒适边距\n"
        f"5. 可以使用描边、投影、色块、渐变底、留白、光影反差等任意方式保证文字和背景有足够对比，但不要固定使用同一种深色底板\n"
        f"6. 人物、物体、图标和装饰元素不能遮挡标题\n\n"
        f"视觉创意要求：\n"
        f"1. 请根据主题自由选择一种合适的视觉风格，例如真实摄影感、电影海报感、插画感、科技感、极简设计、杂志封面、手绘、拼贴、产品海报、社交媒体爆款封面等\n"
        f"2. 色彩方案不要固定，可以根据主题选择冷色、暖色、撞色、低饱和、高饱和、黑金、清新、复古、未来感等不同方向\n"
        f"3. 构图不要模板化，可以使用大主体、局部特写、场景透视、留白排版、对角线构图、分层空间、背景叙事、少量辅助图形等方式\n"
        f"4. 如果描述里包含演示步骤、使用场景或案例，可以用画面元素暗示这些信息；只有在确实需要时再加入小卡片、流程箭头或图标，不要每次都固定做 2-3 个卡片\n"
        f"5. 背景和装饰应服务主题，避免堆砌太多无关科技线条、霓虹光效或小字说明\n\n"
        f"变化要求：\n"
        f"1. 不要生成千篇一律的“中间大字 + 深色底板 + 霓虹科技背景”模板\n"
        f"2. 每次都尝试不同的版式、色彩、主体元素和视觉气质\n"
        f"3. 在保证标题清晰的前提下，让画面更自然、更有设计感、更像人工精心设计的短视频封面\n\n"
        f"限制：\n"
        f"1. 只生成图片内容本身，不要包含 APP 界面、手机边框、点赞评论分享按钮、状态栏或导航栏\n"
        f"2. 不要生成密密麻麻的小字，不要把标题改成英文或其他文案\n"
        f"3. 高清、高质量、竖屏 3:4 封面图"
    )
